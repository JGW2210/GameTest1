// Build the bundled dataset from the curated seed.
//
//   node scripts/build-dataset.mjs
//
// Flattens scripts/seed-taxa.mjs, pushes every record through the updated
// nomenclature normalizer, and writes:
//   • data/bacteria.json  — portable dataset (used by scripts/fetch-gbif.mjs too)
//   • src/data.js         — ES module the browser game imports directly
//
// The output schema is identical to what scripts/fetch-gbif.mjs produces from a
// live GBIF pull, so the two data paths are interchangeable.

import { writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { SEED } from './seed-taxa.mjs';
import { normalizeRecord } from '../src/normalize.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

function buildRecords() {
  const records = [];
  const seen = new Set();
  for (const group of SEED) {
    for (const [genus, species, notes] of group.taxa) {
      const rec = normalizeRecord({
        kingdom: 'Bacteria',
        phylum: group.phylum,
        class: group.klass,
        order: group.order,
        family: group.family,
        genus,
        species,
        notes: notes || '',
      });
      const key = `${rec.genus} ${rec.species}`;
      if (seen.has(key)) continue; // de-duplicate after normalization
      seen.add(key);
      records.push(rec);
    }
  }
  records.sort((a, b) =>
    `${a.genus} ${a.species}`.localeCompare(`${b.genus} ${b.species}`),
  );
  return records;
}

function summarize(records) {
  const phyla = new Set(records.map((r) => r.phylum));
  const genera = new Set(records.map((r) => r.genus));
  return {
    count: records.length,
    phyla: phyla.size,
    genera: genera.size,
    source: 'curated-seed',
    generatedAt: new Date().toISOString(),
  };
}

const records = buildRecords();
const meta = summarize(records);

mkdirSync(join(root, 'data'), { recursive: true });
writeFileSync(
  join(root, 'data', 'bacteria.json'),
  JSON.stringify({ meta, records }, null, 2) + '\n',
);

const banner =
  '// AUTO-GENERATED by scripts/build-dataset.mjs — do not edit by hand.\n' +
  '// Regenerate with:  node scripts/build-dataset.mjs\n';
writeFileSync(
  join(root, 'src', 'data.js'),
  banner +
    `export const META = ${JSON.stringify(meta, null, 2)};\n\n` +
    `export const BACTERIA = ${JSON.stringify(records, null, 2)};\n`,
);

console.log(
  `Built ${meta.count} records · ${meta.genera} genera · ${meta.phyla} phyla`,
);
